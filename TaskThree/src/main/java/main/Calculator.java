package main;

public class Calculator {
    public static long sum (int a, int b){
        return (long) a + b;
    }

    public static long subtract(int a, int b){
        return (long) a - b;
    }

    public static long multiply( int a, int b){
        return (long) a * b;
    }

    public static double divide( int a, int b) { return (double) a / b; }
}
